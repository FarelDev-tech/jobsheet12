public class Kafe08 {
    // Percobaan 1: Membuat fungsi menu()
    public static void menu() {
        System.out.println("=== Kafe Menu ===");
        System.out.println("1. Kopi Hitam - Rp 15000");
        System.out.println("2. Cappucino - Rp 20000");
        System.out.println("3. Latte - Rp 22000");
        System.out.println("4. Teh Tarik - Rp 12000");
    }

    public static void main(String[] args) {
        menu();

        // Menampilkan menu tanpa fungsi
        System.out.println("\n=== Kafe Menu (Tanpa Fungsi) ===");
        System.out.println("1. Kopi Hitam - Rp 15000");
        System.out.println("2. Cappucino - Rp 20000");
        System.out.println("3. Latte - Rp 22000");
        System.out.println("4. Teh Tarik - Rp 12000");

        // Jadi langsung saja tanpa perlu untuk memanggil fungsi menu() atau tanpa fungsi menu()
    }
}